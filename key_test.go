func TestDecodeKeys(t *testing.T) {
	for _, w := range keys {
		if g, err := DecodeKey(w.enc); err != nil {
			t.Fatal(err)
		} else if *g != w.key {
			t.Fatalf("expected %q, got %q", w.key, *g)
		}
	}
}

func TestEncode(t *testing.T) {
	for _, w := range keys {
		g := w.key.Encode()
		if g != w.enc {
			t.Fatalf("expected %q, got %q", w.enc, g)
		}
	}
}
